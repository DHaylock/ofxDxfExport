void ofxDxfExport::writeFile(string path,vector<ofVec3f> pts)
{

    os.open(path+".dxf",ofFile::WriteOnly);
    writeHOF();
    writeTable();
    writeBlock();
    addPoints(pts);
    writeDictionary();
    writeEOF();
    ofSystemAlertDialog("DXF " + path + " Created");
}
